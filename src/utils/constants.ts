// 간단한 한글 숫자 패턴 변환 (예: "이십이점오" → 22.5)
export function koreanToNumber(text: string): number | null {
  // 이미 숫자면 그대로
  const direct = parseFloat(text);
  if (!isNaN(direct)) return direct;

  // 한글 숫자 패턴
  let s = text.trim();

  // "이십이점오" 처리: 십/백/천 포함된 경우
  let result = 0;
  let current = 0;

  for (const [kor, val] of Object.entries(KOREAN_NUMBER_MAP)) {
    s = s.replace(new RegExp(kor, 'g'), val < 10 ? String(val) : (val === 10 ? '10' : String(val)));
  }
  // 점 → .
  s = s.replace('점', '.');
  const n = parseFloat(s);
  return isNaN(n) ? null : n;
}

